export function paddingX(spacing: string) {
  return {
    paddingLeft: spacing,
    paddingRight: spacing,
  };
}

export function paddingY(spacing: string) {
  return {
    paddingTop: spacing,
    paddingBottom: spacing,
  };
}

export function marginX(spacing: string) {
  return {
    marginLeft: spacing,
    marginRight: spacing,
  };
}

export function marginY(spacing: string) {
  return {
    marginTop: spacing,
    marginBottom: spacing,
  };
}
